/**
 * Generate Solidity test fixtures from real ZK proofs.
 *
 * Produces a keccak-based UltraHonk proof (EVM-compatible) for the movement
 * circuit, then writes it as Solidity constants for use in forge tests.
 *
 * Usage: node tests/scripts/generate-test-fixtures.mjs
 */

import { readFile, writeFile, mkdir } from "fs/promises";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";
import { Noir } from "@noir-lang/noir_js";
import { UltraHonkBackend } from "@aztec/bb.js";

const __dirname = dirname(fileURLToPath(import.meta.url));
const CIRCUITS_DIR = resolve(__dirname, "../../circuits/target");
const OUTPUT_DIR = resolve(__dirname, "../../contracts/test");

// -----------------------------------------------------------------------
// Utilities
// -----------------------------------------------------------------------

async function loadCircuit(name) {
  const raw = await readFile(resolve(CIRCUITS_DIR, `${name}.json`), "utf-8");
  return JSON.parse(raw);
}

function toHex(uint8Array) {
  return "0x" + Array.from(uint8Array).map(b => b.toString(16).padStart(2, "0")).join("");
}

function formatBytes(hex, name) {
  return `    bytes internal constant ${name} = hex"${hex.slice(2)}";`;
}

function formatBytes32Array(arr, name) {
  const items = arr.map(v => {
    // publicInputs from bb.js are hex strings, ensure they're 32 bytes
    const hex = v.startsWith("0x") ? v : "0x" + v;
    return `        bytes32(${hex})`;
  });
  return `    function ${name}() internal pure returns (bytes32[] memory r) {
        r = new bytes32[](${arr.length});
${arr.map((v, i) => {
    const hex = v.startsWith("0x") ? v : "0x" + v;
    return `        r[${i}] = bytes32(${hex});`;
  }).join("\n")}
    }`;
}

// -----------------------------------------------------------------------
// Poseidon (matches circomlibjs used in test utils)
// -----------------------------------------------------------------------

let poseidonFn;
async function initPoseidon() {
  const { buildPoseidon } = await import("circomlibjs");
  const p = await buildPoseidon();
  poseidonFn = (inputs) => {
    const h = p(inputs);
    return p.F.toObject(h);
  };
}

function poseidonHash(inputs) {
  return poseidonFn(inputs);
}

function commitPosition(x, y, salt) {
  return poseidonHash([x, y, salt]);
}

// -----------------------------------------------------------------------
// Movement fixture
// -----------------------------------------------------------------------

async function generateMovementFixture() {
  console.log("Generating movement fixture...");
  const circuit = await loadCircuit("movement");
  const noir = new Noir(circuit);
  const backend = new UltraHonkBackend(circuit.bytecode);

  const oldX = 5n, oldY = 5n, oldSalt = 111n;
  const newX = 5n, newY = 6n, newSalt = 222n;
  const direction = 0; // North
  const gridWidth = 32n, gridHeight = 32n;

  const oldCommitment = poseidonHash([oldX, oldY, oldSalt]);
  const newCommitment = poseidonHash([newX, newY, newSalt]);

  const inputs = {
    old_x: oldX.toString(),
    old_y: oldY.toString(),
    old_salt: oldSalt.toString(),
    new_x: newX.toString(),
    new_y: newY.toString(),
    new_salt: newSalt.toString(),
    direction: direction.toString(),
    old_commitment: "0x" + oldCommitment.toString(16),
    new_commitment: "0x" + newCommitment.toString(16),
    grid_width: gridWidth.toString(),
    grid_height: gridHeight.toString(),
  };

  const { witness } = await noir.execute(inputs);
  const proofData = await backend.generateProof(witness, { keccak: true });

  console.log(`  Proof size: ${proofData.proof.length} bytes`);
  console.log(`  Public inputs: ${proofData.publicInputs.length}`);

  await backend.destroy();

  return {
    proof: toHex(proofData.proof),
    publicInputs: proofData.publicInputs,
    // Also return the raw values for the contract to assemble
    oldCommitment: "0x" + oldCommitment.toString(16).padStart(64, "0"),
    newCommitment: "0x" + newCommitment.toString(16).padStart(64, "0"),
    gridWidth: "0x" + gridWidth.toString(16).padStart(64, "0"),
    gridHeight: "0x" + gridHeight.toString(16).padStart(64, "0"),
  };
}

// -----------------------------------------------------------------------
// Output
// -----------------------------------------------------------------------

async function main() {
  await initPoseidon();

  const movement = await generateMovementFixture();

  const sol = `// SPDX-License-Identifier: GPL-3.0
pragma solidity ^0.8.28;

/// @notice Auto-generated ZK proof fixtures for end-to-end Solidity tests.
/// @dev Generated by: node tests/scripts/generate-test-fixtures.mjs
library TestFixtures {
    // -----------------------------------------------------------------------
    // Movement proof (valid move: (5,5) -> (5,6) North, 32x32 grid)
    // -----------------------------------------------------------------------

${formatBytes(movement.proof, "MOVEMENT_PROOF")}

${formatBytes32Array(movement.publicInputs, "movementPublicInputs")}

    // Raw values for contract assembly
    bytes32 internal constant MOVEMENT_OLD_COMMITMENT = ${movement.oldCommitment};
    bytes32 internal constant MOVEMENT_NEW_COMMITMENT = ${movement.newCommitment};
}
`;

  const outPath = resolve(OUTPUT_DIR, "TestFixtures.sol");
  await mkdir(OUTPUT_DIR, { recursive: true });
  await writeFile(outPath, sol, "utf-8");
  console.log(`\nWrote ${outPath}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
